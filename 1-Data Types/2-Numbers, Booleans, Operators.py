# INTEGER

black_tea_grams = 5
pink_tea_grams = 7

total_grams = black_tea_grams + pink_tea_grams
print(total_grams)

remaining_tea = pink_tea_grams - black_tea_grams
print(f"Total grams of remaining tea are {remaining_tea}")


# DIVISION
litres_of_milk = 7
serving_people = 10
print(f"Litres of milk per serving {litres_of_milk / serving_people}")


# BOOLEAN
is_boiling = True
stir_count = 6
total_count = stir_count + is_boiling   # upcasting
print(total_count)