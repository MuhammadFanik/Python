# You want to simulate tea heating. It starts at 40 and boils at 100.
# Task : 1- Use a while loop. 2- Increase the temperature by 15 until it reaches or exceeds 100. 3- Print each temp step

start = 40
boil = 100
while start <= boil:
    print(start)
    start = start + 15

print("Tea is ready to be served")